#include "main.h"
/**
 * add - adds two numbers
 * @a: first number
 * @b: second number
 * Returns: sum of two numbers
 */

int add(int a, int b)
{
    return (a + b);
}

/**
 * sub - adds two numbers
 * @a: first number
 * @b: second number
 * Returns: subtract of two numbers
 */

int sub(int a, int b)
{
    return (a - b);
}

/**
 * mul - adds two numbers
 * @a: first number
 * @b: second number
 * Returns: multiply of two numbers
 */

int mul(int a, int b)
{
    return (a * b);
}

/**
 * add - adds two numbers
 * @a: first number
 * @b: second number
 * Returns: divide of two numbers
 */

double div(int a, int b)
{
    return (a / b);
}

/**
 * add - adds two numbers
 * @a: first number
 * @b: second number
 * Returns: module of two numbers
 */

int mod(int a, int b)
{
    return (a % b);
}